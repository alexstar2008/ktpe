раїни; Круглий стіл “Україна відкрита для співпраці”, 22-23 лютого 2007 р. (співзасновник); “Корупція в Україні: методи боротьби і зарубіжний досвід”, 20 квітня 2015 р.; І Всеукраїнська науково-практична конференція “Глобалізація напрямів формування промислового потенціалу в умовах постіндустріальних трансформацій“, 23 березня 2016 р.; ІІ Всеукраїнська науково-практична конференція з міжнародною участю “Глобалізація напрямів формування промислового потенціалу в умовах постіндустріальних трансформацій“, 20 квітня 2017 р. (Співорганізатори: Вища школа економіки в Бигдощі (Польща); Українсько-польський ВНЗ «Центрально-Європейський університет»)  Видається щорічний збірник наукових праць “Глобалізація напрямів формування промислового потенціалу в умовах постіндустріальних трансформацій”. За  25 років на кафедрі захищено 28 дисертаційних робіт.
