    			<h4>Історія кафедри</h4>
	    			<p>З 1991 року кафедру очолював к.т.н, доцент Богданюк Віталій Євгенович. Під його керівництвом на кафедрі було розроблено та інтегровано у навчальний процес ряд спеціалізованих курсів для студентів технічних спеціальностей, які розширювали розуміння сучасних економічних процесів у майбутніх інженерів, а також формували у них ринкове відношення до інженерних рішень.</p>
					<p>У 1992 році кафедра увійшла до складу новоствореного факультету менеджменту та маркетингу. Викладачі кафедри почали не тільки забезпечувати економічну складову інженерної освіти, а і активно приймати участь у підготовці фахівців економічних спеціальностей факультету. Зокрема, кафедра спеціалізувалась на викладанні базових економічних дисциплін, таких як «Гроші та кредит», «Економіка підприємства», «Статистика», «Бухгалтерський облік та аудит», «Планування виробництва», тощо,  для всіх економічних спеціальностей факультету.</p>
					<p>З  2002 рокувже під керівництвом доктора економічних наук, професора Ялового Гаррі Кириловича кафедра перейменована в кафедру теоретичної та прикладної  економіки. Починається період активної підготовки наукових кадрів, відкривається власна аспірантура. Випускники наукової школи кафедри стають кандидатами, а в подальшому і докторами економічних наук, викладачами вищих навчальних закладів, науковцями, отримують звання доцентів і професорів.</p>
					<p>З 2015 року завідувачем кафедри є доктор економічних наук, професор Крейдич Ірина Миколаївна. В цьому ж році на кафедрі відкрита Спеціалізація «Управління персоналом та економіка праці». Розпочинається підготовка фахівців даного напряму у співпраці з провідними європейськими університетами, а також активна робота по впровадженню освітньої  програми з отриманням студентами подвійного диплому у співпраці з одним із європейських вишів. </p>
